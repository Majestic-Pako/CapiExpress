            <section>
                <h2>El Origen</h2>
                <p>
                    La historia de CapiExpress comienza en un pequeño rincón de la ciudad, donde se encontraba un espacio sencillo, pero lleno de potencial. En una zona transitada, donde las personas siempre buscaban un lugar rápido, pero con calidad, para disfrutar de una buena bebida caliente o fría, nació este concepto que transformaría la forma en que la comunidad disfrutaba el café.
                </p>
                <p>
                    CapiExpress nació hace unos 10 años, cuando su fundadora, Mariana López, una joven barista con experiencia en varias cafeterías de renombre, se dio cuenta de una creciente necesidad entre los trabajadores urbanos: un espacio que combinara rapidez, buen café y una experiencia agradable en poco tiempo.
                </p>
                <p>
                    Con la intención de brindar un servicio de calidad sin perder la rapidez, Mariana ideó CapiExpress como una cafetería que ofrecería no solo el café de siempre, sino una propuesta diferente. Sabía que en la vida diaria las personas no siempre tienen el lujo de detenerse en una cafetería por horas, pero aún así merecían disfrutar de una taza excelente.
                </p>
                <img src="imagen de rapidezycalidad.avif" alt="Cafetería moderna" />
            </section>
    
            <section>
                <h2>El Concepto de Rapidez y Calidad</h2>
                <p>
                    CapiExpress se estableció bajo el lema "El sabor que te lleva". Este lema se refería no solo a la rapidez del servicio, sino también a la calidad del café, un café que no sacrificaba su sabor, ni su aroma, por la velocidad. Desde el principio, Mariana seleccionó granos de café de origen local, buscando pequeños productores que ofrecieran un producto fresco y de alta calidad.
                </p>
                <p>
                    Además, se incluyó una variedad de opciones para diferentes gustos: desde los clásicos espressos hasta opciones más sofisticadas como lattes de sabores, capuchinos con arte latte y bebidas frías de café. El local en sí era moderno, pequeño pero acogedor, con un ambiente minimalista que permitía que los clientes se sintieran cómodos mientras esperaban su café para llevar o lo consumían en una mesa.
                </p>
                <p>
                    La idea era ofrecer algo más que un simple "café rápido"; CapiExpress se convirtió en un lugar donde las personas podían disfrutar de una pausa, aunque fuera breve.
                </p>
                <img src="img/local.png" alt="Cafetería moderna" />
            </section>
    
            <section>
                <h2>Innovación y Expansión</h2>
                <p>
                    A medida que el tiempo pasaba, CapiExpress no solo ganó popularidad en el vecindario, sino que comenzó a captar la atención de un público más amplio. La gente comenzó a referirse a ella como la cafetería que lograba ofrecer un buen café en menos de 3 minutos, lo cual era un logro para el mundo de las cafeterías, que usualmente priorizaban el "slow coffee" o el café elaborado con más tiempo.
                </p>
                <p>
                    Pero la verdadera clave del éxito de CapiExpress estuvo en su capacidad para adaptarse a las nuevas tendencias. Durante la pandemia, Mariana innovó ofreciendo un sistema de pedidos en línea y un servicio de entrega eficiente. La cafetería también lanzó su propia línea de cafés para preparar en casa, una opción que fue muy bien recibida por quienes querían disfrutar del sabor de CapiExpress desde la comodidad de su hogar.
                </p>
            </section>
    
            <section>
                <h2>CapiExpress Hoy</h2>
                <p>
                    Hoy en día, CapiExpress es una marca conocida en la ciudad, con varias sucursales en diferentes puntos. El menú sigue siendo una combinación de clásicos y nuevas tendencias: café de especialidad, bebidas veganas, opciones sin gluten, y una selección de repostería artesanal.
                </p>
                <p>
                    Cada local mantiene el mismo enfoque en la rapidez y calidad, con un equipo de baristas capacitados que se esmeran en crear experiencias memorables. Además, CapiExpress se ha involucrado en iniciativas comunitarias, apoyando a pequeños agricultores y promoviendo el comercio justo.
                </p>
                <p>
                    La marca ha crecido más allá de la simple venta de café, convirtiéndose en un punto de encuentro para personas que aprecian el buen café, el buen servicio y el impacto positivo en la comunidad. CapiExpress continúa siendo el lugar donde los sabores se encuentran con la rapidez, y donde cada taza cuenta una historia: la de una pequeña idea que creció gracias al amor por el café y la dedicación a las personas que lo disfrutan.
                </p>
